package Listas;

public class Pares {
    int r;
    int c;

    public Pares(int r, int c) {
        this.r = r;
        this.c = c;
    }

    public Pares() {
    }


    public int getR() {
        return r;
    }

    public void setR(int r) {
        this.r = r;
    }

    public int getC() {
        return c;
    }

    public void setC(int c) {
        this.c = c;
    }
}
